package org.example.LoopingStatements.WhileLoop;

public class WhileNinthTable
{
    public static void main(String[] args)
    {
        int z=0;
        int x=9;
        int i=1;
        while(i<=10)
        {
            z=x*i;
            System.out.println(x+"*"+i+"="+z);
            i++;
        }
    }
}
